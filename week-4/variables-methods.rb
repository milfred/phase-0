# How do you define a local variable?
# You declare the variable name, type an equal sign, and then enter the variable (e.g. local_var = "the variable").

# How do you define a method?
# To define a method you type "def," then add a space, and then define the name of the method. If the method has arguements you would include them after the name. The arguements can be inside parentheses, but they don't have to be as long as there is a space after the name (e.g. def method(arguement1, arguement2)).

# What is the difference between a local variable and a method?
# A local variable stores information for later use by calling the name of the variable. A method is code that performs an action.

# How do you run a ruby program from the command line?
# To run a ruby program from the command line you simply type "ruby name_of_program.rb"

# How do you run an RSpec file from the command line?
# To run an RSpec file from the command line type "rspec name_of_file.rb"

